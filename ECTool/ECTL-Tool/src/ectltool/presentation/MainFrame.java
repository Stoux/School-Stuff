    private void selecteerBestandButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selecteerBestandButtonActionPerformed
        JFileChooser chooser = new JFileChooser();
        if (selectedFile != null) {
            chooser.setCurrentDirectory(selectedFile);
        }
        int result = chooser.showOpenDialog(this);
        
        if (result == JFileChooser.APPROVE_OPTION) {
            selectedFile = chooser.getSelectedFile();
            bestandField.setText(selectedFile.getName());
            _.log("Selected file: " + selectedFile.getAbsolutePath());
            if (!selectedListValue.equals("")) {
                startButton.setEnabled(true);
            }
        }
    }//GEN-LAST:event_selecteerBestandButtonActionPerformed

    private void startButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_startButtonActionPerformed
        if (selectedFile == null || selectedListValue.equals("")) {
            startButton.setEnabled(false);
            return;
        }
                
        JFrame f;
        
        switch (selectedListValue) {
            case "MySQL DB":
                _.log("File type not supported yet.");
                return;
            case "Char Seperated Text File":
                f = new CharSepratedFrame();
                break;
            case "Microsoft Excel File":
                f = new ExcelFrame();
                break;
            case "Microsoft Acces File":
                f = new AccesFrame();
                break;
            case "Space Seperated Text File":
                f = new SpaceSeperatedFrame();
                break;
            default:
                return;
        }
        setEnabled(false);
        f.setVisible(true);
        f.setLocationRelativeTo(this);
    }//GEN-LAST:event_startButtonActionPerformed

