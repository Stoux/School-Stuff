    public void parseFile(final FileReader fr) {
        toFront();
        new Thread(new Runnable() {

            @Override
            public void run() {
                currentOpdracht.getFileModel().startedECTL();
                fr.extract();
                fr.clean();
                fr.transform();
                fr.load();
                currentOpdracht.getFileModel().endedECTL();
                startButton.setEnabled(true);
                setEnabled(true);
            }
        }).start();
    }
    
    private String selectedListValue = "";
    
    private void typeListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_typeListValueChanged
        String newValue = (String) typeList.getSelectedValue();
        if (!newValue.equals(selectedListValue)) {
            _.log("Selected file type: " + newValue);
            selectedListValue = newValue;
            if (selectedFile != null) {
                startButton.setEnabled(true);
            }
        }
    }//GEN-LAST:event_typeListValueChanged

